package com.tut1411;

public class Assi1_1411 {
	public static void main(String[] args) {
		System.out.println("Java Day2 Practice set");//String literal/constant
		System.out.println(141122);//Numeric constant
		System.out.println(14.1122);//Numeric Decimal constant
		System.out.println(14+11);
		System.out.println(14-11);
		System.out.println(14*11);
		System.out.println(14/11);
		System.out.println(14%11);
		System.out.println("14==14");//String literal
		System.out.println(14==14);
		System.out.println(14==11);
		System.out.println(true);//Boolean literal
		System.out.println(false);
		System.out.println(true==true);
		System.out.println(true==false);
		System.out.println('S');// Char constant
		System.out.println('4');
		System.out.println('_');
		System.out.println('S' == 'S');
		System.out.println('S' == 's');
	}
}
